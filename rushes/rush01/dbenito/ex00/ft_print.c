#include "slib.h"

void	ft_print(char **sudoku)
{
	int	i;
	int	j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			ft_putchar(sudoku[i][j] + '0');
			if (j != 8)
				ft_putchar(' ');
			++j;
		}
		ft_putchar('\n');
		++i;
	}
}
