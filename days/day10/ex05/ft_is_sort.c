int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	if (length <= 1)
		return (1);
	while (--length)
	{
		if (f(*tab, tab[1]) > 0)
			return (0);
		++tab;
	}
	return (1);
}
