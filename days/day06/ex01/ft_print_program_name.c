void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
}

int		main(int argc, char **argv)
{
	if (!argc)
		return (0);
	ft_putstr(argv[0]);
	ft_putchar('\n');
	return (0);
}
