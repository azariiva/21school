void	ft_swap(int *a, int *b)
{
	int c;

	c = *a;
	*a = *b;
	*b = c;
}

int		ft_antidote(int i, int j, int k)
{
	if (i < j)
		ft_swap(&i, &j);
	if (i < k)
		ft_swap(&i, &k);
	if (j < k)
		ft_swap(&j, &k);
	return (j);
}
